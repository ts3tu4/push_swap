#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*newnode;
	t_list	*newlst;

	if ((*f) == NULL || (*del) == NULL)
		return (NULL);
	newnode = NULL;
	newlst = NULL;
	while (lst != NULL)
	{
		newnode = ft_lstnew((f)(lst->content));
		if (newnode == NULL)
		{
			ft_lstclear(&newlst, del);
			return (NULL);
		}
		ft_lstadd_back(&newlst, newnode);
		lst = lst->next;
	}
	return (newlst);
}
